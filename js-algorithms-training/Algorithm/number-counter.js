/**
 * Kata: Sequence Generator (1 to 100)
 * Level: Basic Logic / Loops
 * Platform: Personal Training
 *
 * Description:
 * Uses a while loop to iterate and print all numbers starting
 * from 1 up to 100.
 */

function countToHundred() {
  let start = 0;

  while (start < 100) {
    start++;
    console.log(start);
  }
}

countToHundred();
